// Write a program to print the sum of first 10 natural numbers

class Exercise1 {

	public static void main(String args[]) {
    int n=10;
    int sum=0;
    for(int i=1;i<=n;i++){
      sum+=1;
        System.out.println("Sum of the first " + n + " natural numbers is: " + sum);
    }
}
}


      

